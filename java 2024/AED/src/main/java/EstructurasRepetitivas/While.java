public class While {
    
    
    public static void main(String[] args) {
        
        
        //ciclo while (mientras)
        
        
       int x=0;
       
       while(x<=10)
       {
           System.out.println("El valor de x es:" + x);
           
           //una vez ejecutado debo sumarle en 1 para que x cambie sino se ejcutar un bucle infinito
           
           x+=1;
       }
       
       
       
        System.out.println("");
       
    }
}
