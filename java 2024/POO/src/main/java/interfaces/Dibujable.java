/*

Una interfaz en Java define un conjunto de métodos que una clase concreta 
debe implementar. Especifica qué métodos debe tener una clase, 
pero no proporciona la implementación de esos métodos. 
Las interfaces son utilizadas para definir un contrato entre las clases,
lo que garantiza que las clases que implementan la interfaz proporcionen 
cierto comportamiento.
*/



