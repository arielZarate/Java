
/**
 * Clase que realiza operaciones aritmeticas como suma, resta division, etc
 *
 * @author Ubaldo Acosta
 */
public class Artimetica {

    /**
     * Primer operando
     */
    int a;
    /**
     * Segundo operando
     */
    int b;

    /**
     * Constructor de la clase
     */
    public Artimetica() {

    }

    /**
     * Metodo que suma dos enteros
     *
     * @return int resutaldo de la suma de los operandos
     */
    public int sumar() {
        return a + b;
    }
}
